# create tuples
my_tuple = ("this", "is", "a", "tuple")
print(my_tuple)

# Access list items
print(my_tuple[0])
print(my_tuple[1])
print(my_tuple[2])
print(my_tuple[-1])
print(my_tuple[-2])
print(my_tuple[1:])
print(my_tuple[1:3])
print(my_tuple[:3])

# index
index = my_tuple.index('is')
print(index)

